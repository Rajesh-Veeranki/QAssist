package com.aneedo.indexing.bean;

public class WikiLink {
	String leftContext, rightContext, linkText;
	



	public String getLeftContext() {
		return leftContext;
	}

	public void setLeftContext(String leftContext) {
		this.leftContext = leftContext;
	}

	public String getRightContext() {
		return rightContext;
	}

	public void setRightContext(String rightContext) {
		this.rightContext = rightContext;
	}

	public String getLinkText() {
		return linkText;
	}

	public void setLinkText(String linkText) {
		this.linkText = linkText;
	}

	public WikiLink(String linkText,String leftContext,String rightContext) {
		this.leftContext = leftContext;
		this.linkText = linkText;
		this.rightContext = rightContext;
	}
	public WikiLink(String linkText) {
		this.linkText = linkText;
	}

	public WikiLink() {

	}

}
